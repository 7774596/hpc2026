#!/usr/bin/env bash
# Run the simulation experiments used by the report.
#
# Usage:
#   bash scripts/run_report_experiments.sh [output_root]
#
# Tunable environment variables:
#   SEEDS=7,11,13
#   BASE_CONFIG=configs/medium.ini
#   PYTHON=/path/to/python
set -euo pipefail
cd "$(dirname "$0")/.."

OUT_ROOT=${1:-results/report_experiments}
SEEDS=${SEEDS:-7,11,13}
BASE_CONFIG=${BASE_CONFIG:-configs/medium.ini}
if [[ -z "${PYTHON:-}" ]]; then
    if command -v python3 >/dev/null 2>&1; then
        PYTHON=python3
    elif command -v python >/dev/null 2>&1; then
        PYTHON=python
    else
        echo "python interpreter not found; load a Python module or set PYTHON=/path/to/python" >&2
        exit 1
    fi
fi
echo "python: $("$PYTHON" --version 2>&1)"

make serial
mkdir -p "$OUT_ROOT"

run_and_plot() {
    local name=$1
    local x_col=$2
    shift 2
    local out_dir="$OUT_ROOT/$name"

    "$PYTHON" scripts/run_sweep.py \
        --base-config "$BASE_CONFIG" \
        --output "$out_dir" \
        --experiment "$name" \
        --seeds "$SEEDS" \
        --clean \
        "$@"

    "$PYTHON" scripts/plot_results.py "$out_dir/aggregate.csv" \
        --out-dir "$out_dir/plots" \
        --x "$x_col" \
        --series scheduler
}

echo "=== Experiment 1: scheduler comparison under different arrival rates ==="
run_and_plot arrival_rate arrival_rate \
    --schedulers fcfs,sjf,rr,backfill \
    --num-jobs 20000 \
    --arrival-rates 4,8,12 \
    --nodes 16 \
    --rr-quantums 50

echo "=== Experiment 2: scalability with number of jobs ==="
run_and_plot job_scale num_jobs \
    --schedulers fcfs,sjf,rr,backfill \
    --num-jobs 10000,20000,50000 \
    --arrival-rates 8 \
    --nodes 16 \
    --rr-quantums 50

echo "=== Experiment 3: cluster-size sensitivity ==="
run_and_plot node_scale nodes \
    --schedulers fcfs,sjf,rr,backfill \
    --num-jobs 20000 \
    --arrival-rates 8 \
    --nodes 8,16,32 \
    --rr-quantums 50

echo "=== Experiment 4: RR quantum sensitivity ==="
run_and_plot rr_quantum rr_quantum \
    --schedulers rr \
    --num-jobs 20000 \
    --arrival-rates 8 \
    --nodes 16 \
    --rr-quantums 10,25,50,100,200

cat >"$OUT_ROOT/README.txt" <<EOF
Generated by scripts/run_report_experiments.sh

Each subdirectory contains:
  raw_results.csv   one row per run
  aggregate.csv     mean/std across seeds
  plots/report.html dependency-free SVG report
  plots/*.png       present only when matplotlib is installed

Use aggregate.csv files for report tables and plots/report.html for figures.
EOF

echo "all simulation experiments finished -> $OUT_ROOT"
